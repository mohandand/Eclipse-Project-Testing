package Collections;

public class AutoBoxing_Wrapping {

	public static void main(String[] args) {
		
		int var =20;
		
		//wraping above primitive variable as object using Integer Class
		
		Integer obj = new Integer(var);//Wrapping or Auto boxing Converting primitive into object
		
		System.out.println(obj);
		
		int i =obj;//Unwrapping or AutoUnboxing or Unboxing Converting Objct into Primitive
		
		
		
	}

}
